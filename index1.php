<script>
document.addEventListener('DOMContentLoaded', function () {
    fetch('Live_TV_india.m3u')
        .then(response => response.text())
        .then(data => {
            const lines = data.trim().split('\n');
            const channels = [];
            let channelInfo = {};

            lines.forEach(line => {
                if (line.startsWith('#EXTINF')) {
                    const nameMatch = line.match(/="([^"]*)"/);
                    const logoMatch = line.match(/tvg-logo="([^"]*)"/);

                    if (nameMatch) {
                        channelInfo.name = nameMatch[1];
                        channelInfo.logo = logoMatch ? logoMatch[1] : null;
                    }
                } else if (line.startsWith('http')) {
                    channelInfo.url = line.trim();
                    channels.push({ ...channelInfo }); // Use spread operator to avoid reference issues
                    channelInfo = {};
                }
            });

            const channelsGrid = document.getElementById('channelsGrid');
            channels.forEach(channel => {
                const channelDiv = document.createElement('div');
                channelDiv.classList.add('channel');

                if (channel.logo) {
                    // Create and style the logo image
                    const logoImg = document.createElement('img');
                    logoImg.src = channel.logo;
                    logoImg.alt = channel.name;
                    logoImg.classList.add('channel-logo');
                    channelDiv.appendChild(logoImg);
                } else {
                    // Create and style the name if no logo is available
                    const nameDiv = document.createElement('div');
                    nameDiv.classList.add('channel-name');
                    nameDiv.textContent = channel.name;
                    channelDiv.appendChild(nameDiv);
                }

                // Add event listener to channelDiv
                channelDiv.addEventListener('click', function () {
                    // Update video player source and play
                    const videoElement = document.getElementById('videoPlayer');

                    if (Hls.isSupported()) {
                        const hls = new Hls();
                        hls.loadSource(channel.url);
                        hls.attachMedia(videoElement);

                        hls.on(Hls.Events.MANIFEST_PARSED, function () {
                            videoElement.play();
                        });
                    } else if (videoElement.canPlayType('application/vnd.apple.mpegurl')) {
                        videoElement.src = channel.url;
                        videoElement.addEventListener('loadedmetadata', function () {
                            videoElement.play();
                        });
                    } else {
                        console.error('HLS is not supported in this browser');
                    }

                    // Scroll smoothly to the footer
                    document.getElementById('footer_top').scrollIntoView({ behavior: 'smooth' });
                });

                channelsGrid.appendChild(channelDiv);
            });
        })
        .catch(error => {
            console.error('Error loading playlist:', error);
        });
});
</script>

